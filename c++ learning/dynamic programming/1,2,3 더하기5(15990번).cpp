#include <iostream>
using namespace std;
long dp[100001][4];
int main(void){
    int T;
    cin>>T;
    dp[1][1]=1;
    dp[2][2]=1;
    dp[3][3]=1;
    dp[3][2]=1;
    dp[3][1]=1;
    for(int i=4;i<100001;i++){
            dp[i][1]=(dp[i-1][2]%1000000009+dp[i-1][3]%1000000009)%1000000009;
            dp[i][2]=(dp[i-2][1]%1000000009+dp[i-2][3]%1000000009)%1000000009;
            dp[i][3]=(dp[i-3][1]%1000000009+dp[i-3][2]%1000000009)%1000000009;
    }
    for(int tc=1;tc<=T;tc++){
        int N;
        cin>>N;
        cout<<(dp[N][1]%1000000009+dp[N][2]%1000000009+dp[N][3]%1000000009)%1000000009<<'\n';
    }
    return 0;
}
